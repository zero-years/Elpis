<template>
  <el-sub-menu :index="menuItem">
    <template #title>
      {{ menuItem.name }}
    </template>
    <div v-for="item in menuItem.subMenu" :key="item.key">
      <!-- 如果 item 中还有 subMenu，则递归 -->
      <sub-menu 
        v-if="item.subMenu && item.subMenu.length > 0"
        :menuItem="item"
      ></sub-menu>
      <!-- 如果没有 subMenu 则正常执行 el-menu-item -->
      <el-menu-item
        v-else
        :index="item.key" 
      >
        {{ item.name }}
      </el-menu-item>
    </div>
  </el-sub-menu>
</template>

<script setup>
const { menuItem } = defineProps(['menuItem'])
</script>

